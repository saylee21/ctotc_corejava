public class DecisionMakingIfElse {
    public static void main(String[] args) {
	int numFirst = 5;
    if(numFirst == 5){
		System.out.println("numFirst equals to 5");
	    }
	else if(numFirst > 5){
		System.out.println("numFirst is greater to 5");		
		}
	else {
		System.out.println("numFirst is smaller than 5");
		}
		
	}

}
